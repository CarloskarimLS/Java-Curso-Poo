public class fecha {
    //atributos
    private int dia;
    private String mes;
    private int anio;

    public int getDia(){
        return dia;
    }

    public void setDia(int dia){
        this.dia = dia;
    }

    public String getMes(){
        return mes;
    }

    public void setMes(String mes){
        this.mes = mes;
    }

    public int getAnio(){
        return anio;
    }

    public void setAnio(int anio){
        this.anio = anio;
    }


}
